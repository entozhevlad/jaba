package com.example.kvdb.api;

/**
 * Метрики базы данных.
 * Позволяет получить информацию о текущем состоянии (размер данных и число ключей).
 */
public interface Metrics {
    /**
     * Общее количество ключей во всех таблицах.
     */
    long getKeyCount();

    /**
     * Примерный размер всех данных (сумма размеров ключей и значений) в байтах.
     */
    long getDataSize();
}
